package com.hs.hsblog.pojo;

/**
 * @author Hs
 * @Date 2021/9/22 19:59
 */
public class Student {
    private int Id;
    private String Name;
    private int age;

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
